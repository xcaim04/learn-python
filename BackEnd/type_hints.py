### Type Hints ###

my_string_variable = "My string variable"
print(my_string_variable)
print(type(my_string_variable))

my_string_variable = 5
print(my_string_variable)
print(type(my_string_variable))

# variable: type = value
my_typed_variable: str = "My typed variable"
print(my_typed_variable)

my_number: int = 15
print(my_number)

# Con esta sintaxis puedo sugerir el tipo, pero
# no obliga a tener el tipo de dato que asignas.

# Una de las ventajas que tiene es que el editor
# interpreta que hay un tipo especifico y nos da autocompletado
# especifico a ese tipo. Usando la notacion de punto

